package mangei.cashmanagement.pojo;

public class Buchungskategorie {

	private Long id;
	private String name;
	
	public Buchungskategorie() {
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
